function getAreaOfCircle (a){
    return a / 2 * Math.PI;
}

function getCircumferenceOfCircle ( b) {
    return b / 2 * Math.PI;
}
function getAreaOfsquare (s){
    return (s-2) * 180;
}
function getAreaOfTriangle(b, h){
    return b * h;
}


